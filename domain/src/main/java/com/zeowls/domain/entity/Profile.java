package com.zeowls.domain.entity;

public class Profile {
    private String title;
    private int image;

    public Profile() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
